const fs = require('fs');
const path = require('path');

const projectRoot = path.join(__dirname, '..');
const mainScriptPath = path.join(projectRoot, 'assets', 'js', 'main.js');
const shellScriptPath = path.join(projectRoot, 'assets', 'js', 'site-shell.js');
const source = fs.readFileSync(mainScriptPath, 'utf8');

function sectionStart(marker) {
  const index = source.indexOf(marker);
  if (index === -1) throw new Error(`Unable to build site shell: missing marker ${marker}`);
  return index;
}

const teamsStart = sectionStart('// A small list of sample teams');
const signupStart = sectionStart('// Signup page: toggle between seeker');
const initializeStart = sectionStart('// Initialize on load');
const sharedFooterStart = sectionStart('function loadSharedFooter()');

const commonFunctions = source.slice(0, teamsStart).trimEnd();
const signupFunctions = source.slice(signupStart, initializeStart).trim();
const shellFunctions = source.slice(sharedFooterStart).trim();
const initializeShell = `document.addEventListener('DOMContentLoaded', () => {
  initPageAnimations();
  loadSharedFooter();
  loadSiteShells();
  initJoinForm();
  initAuthGatedActions();
  initSignupForm();
});`;

const output = [
  '// Generated by scripts/build-site-shell.js. Edit assets/js/main.js, then rebuild.',
  commonFunctions,
  signupFunctions,
  initializeShell,
  shellFunctions,
  ''
].join('\n\n');

fs.writeFileSync(shellScriptPath, output, 'utf8');
console.log(`Built ${path.relative(projectRoot, shellScriptPath)} (${Buffer.byteLength(output)} bytes)`);
